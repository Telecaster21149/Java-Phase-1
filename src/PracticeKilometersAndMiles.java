public class PracticeKilometersAndMiles {
    public static void main(String[] args) {
    double k, m, KItoMI;
    m = 1.609344;
    k = 155.7;
    System.out.println();
    System.out.println("Kilometers = " + k);
    System.out.println();
    System.out.println("To calculate the number of miles we need to divide the kilometers by " + m);
    System.out.println();
    KItoMI = k / m;
    System.out.print("155.7 / 1.609344 = " + KItoMI);
    }
}
