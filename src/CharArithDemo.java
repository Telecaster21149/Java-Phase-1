//Symbolic variables can be treated as integers.
public class CharArithDemo {
    public static void main(String[] args) {
        char Ch;
        Ch = 'X';
        System.out.println("Ch contains " + Ch);
        Ch++; //incrementing ch
        System.out.println("Second contains " + Ch);
        Ch = 90; //assigning ch the value Z
        System.out.println("Third contains " + Ch);
    }
}
