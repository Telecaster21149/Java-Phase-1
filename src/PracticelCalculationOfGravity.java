public class PracticelCalculationOfGravity {
    public static void main(String[] args) {
        double Kg, KgOnMoon;
        Kg = 71;
        System.out.println("The gravity on the moon is about 17% of Earth's, so multiplying our weight by 0.17 gives us our weight if we were on the moon.");
        KgOnMoon = Kg * 0.17;
        System.out.println("If we weigh 71 on Earth, then on the moon the value = "+ KgOnMoon);
    }
}
