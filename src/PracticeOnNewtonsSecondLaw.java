public class PracticeOnNewtonsSecondLaw {
    public static void main(String[] args) {
    double a, m, F;
    a = 5.3;
    m = 123.5;
    F = a * m;
    System.out.println("mass = " + m);
    System.out.println("acceleration = " + a);
    System.out.println();
    System.out.println("force = acceleration * mass = " + F + " N");
    }
}
