/*about int and double
*/
public class Example3 {
    public static void main(String[] arg) {
        int v; // declaration of an integer variable
        double x; // declaration of a floating-point variable
        v = 10;
        x = 10.0;
        System.out.println("Original meaning v: " + v);
        System.out.println("Original meaning x: " + x);
        System.out.println();
        v = 10 / 4;
        x = 10.0 / 4;
        System.out.println("v after division: " + v);
        System.out.println("x after division: " + x);
    }
}
